/**
 * Scans /public/Stickers/ and generates a manifest mapping profession names to sticker paths.
 * Runs as prebuild/predev hook — any new sticker file automatically appears in the store.
 *
 * Naming convention: filename "software-engineer.png" → profession "Software Engineer"
 * The manifest is written to src/generated/sticker-manifest.ts as a typed export.
 */

const fs = require('fs');
const path = require('path');

const STICKERS_DIR = path.join(__dirname, '..', 'public', 'Stickers');
const OUTPUT_DIR = path.join(__dirname, '..', 'src', 'generated');
const OUTPUT_FILE = path.join(OUTPUT_DIR, 'sticker-manifest.ts');

// Convert filename to title case profession name
// "software-engineer.png" → "Software Engineer"
// "ai-ml-specialist.png" → "AI/ML Specialist"
// "ux-designer.png" → "UX/UI Designer"
function filenameToProfession(filename) {
  const base = filename.replace(/\.\w+$/, ''); // strip extension

  // Special mappings for names that don't follow simple hyphen-to-space convention
  const SPECIAL_MAPPINGS = {
    'ceo': 'CEO',
    'ai-ml-specialist': 'AI/ML Specialist',
    'ux-designer': 'UX/UI Designer',
    'dj': 'DJ',
    'qa-engineer': 'QA Engineer',
    'hr-manager': 'HR Manager',
    'nutritionist-food': 'Nutritionist',
    'culinary-beverage': 'Sommelier',
    'fitness': 'Fitness Coach',
  };

  if (SPECIAL_MAPPINGS[base]) return SPECIAL_MAPPINGS[base];

  // Default: hyphen to space, capitalize each word
  return base
    .split('-')
    .map(word => word.charAt(0).toUpperCase() + word.slice(1))
    .join(' ');
}

function generate() {
  if (!fs.existsSync(STICKERS_DIR)) {
    console.warn('[sticker-manifest] No Stickers directory found, generating empty manifest');
    fs.mkdirSync(OUTPUT_DIR, { recursive: true });
    fs.writeFileSync(OUTPUT_FILE, 'export const STICKER_MANIFEST: Record<string, string> = {};\n');
    return;
  }

  const files = fs.readdirSync(STICKERS_DIR).filter(f => /\.(png|jpg|jpeg|svg|webp)$/i.test(f));

  // Build mapping: profession name → sticker path
  // If multiple files map to the same profession (e.g. actor.png and actor-2.png),
  // prefer the one without a numeric suffix
  const manifest = {};
  const seen = new Set();

  // Sort so base names come before variants (actor.png before actor-2.png)
  files.sort((a, b) => a.length - b.length);

  for (const file of files) {
    // Skip Gemini-generated files (temp/test assets)
    if (file.startsWith('Gemini_')) continue;

    const profession = filenameToProfession(file);
    if (!seen.has(profession)) {
      manifest[profession] = `/Stickers/${file}`;
      seen.add(profession);
    }
  }

  fs.mkdirSync(OUTPUT_DIR, { recursive: true });

  const entries = Object.entries(manifest)
    .sort(([a], [b]) => a.localeCompare(b))
    .map(([prof, path]) => `  ${JSON.stringify(prof)}: ${JSON.stringify(path)}`)
    .join(',\n');

  const content = `// Auto-generated by scripts/generate-sticker-manifest.js — do not edit manually
// Re-generated on every build/dev start. Add stickers to /public/Stickers/ to update.

export const STICKER_MANIFEST: Record<string, string> = {
${entries},
};

/**
 * Get sticker path for a profession. Returns the path if a sticker exists, undefined otherwise.
 * Tries exact match first, then normalized match (lowercase hyphenated).
 */
export function getStickerPath(profession: string): string | undefined {
  if (STICKER_MANIFEST[profession]) return STICKER_MANIFEST[profession];

  // Fallback: try to derive path from profession name
  const derived = '/Stickers/' + profession.toLowerCase().replace(/[/\\s]+/g, '-').replace(/[^a-z0-9-]/g, '') + '.png';
  // Check if derived key exists in manifest values
  const match = Object.values(STICKER_MANIFEST).find(p => p === derived);
  return match || undefined;
}
`;

  // Only write if content changed (avoid unnecessary rebuilds)
  const existing = fs.existsSync(OUTPUT_FILE) ? fs.readFileSync(OUTPUT_FILE, 'utf8') : '';
  if (existing !== content) {
    fs.writeFileSync(OUTPUT_FILE, content);
    console.log(`[sticker-manifest] Generated ${Object.keys(manifest).length} sticker mappings`);
  } else {
    console.log(`[sticker-manifest] No changes (${Object.keys(manifest).length} stickers)`);
  }
}

generate();
